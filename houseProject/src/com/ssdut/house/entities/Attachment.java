package com.ssdut.house.entities;

public class Attachment {
	private String id;
	private String attachmentOrgName;
	private String attachmentReName;
	private String caseId;
	private String state;
	
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAttachmentOrgName() {
		return attachmentOrgName;
	}
	public void setAttachmentOrgName(String attachmentOrgName) {
		this.attachmentOrgName = attachmentOrgName;
	}
	public String getAttachmentReName() {
		return attachmentReName;
	}
	public void setAttachmentReName(String attachmentReName) {
		this.attachmentReName = attachmentReName;
	}
	public String getCaseId() {
		return caseId;
	}
	public void setCaseId(String caseId) {
		this.caseId = caseId;
	}
	
}
